@checklist.command("list")
@click.argument("project_dir", type=click.Path())
@click.pass_context
def checklist_list(ctx, project_dir):
    """List all checklists (in-progress and completed)."""
    project_path = Path(project_dir).resolve()
    logger = StructuredLogger(project_path)
    engine = ChecklistEngine(project_path, logger)

    checklists = engine.get_checklists()

    if not checklists:
        click.echo("No checklists found")
        return

    click.echo(f"📋 Checklists for {project_path.name}")
    for checklist in checklists:
        click.echo(f"\n  ID:      {checklist['checklist_id']}")
        click.echo(f"  Time:    {checklist['timestamp']}")
        click.echo(f"  Task:    {checklist['task_name']}")
        click.echo(f"  Step:    {checklist['step_index']}")
        click.echo(f"  Done:    {', '.join(checklist['completed_tasks'])}")


@checklist.command("run")
@click.argument("project_dir", type=click.Path())
@click.option("--dry-run", is_flag=True, help="Don't make changes")
@click.option("--verbose", is_flag=True, help="Show detailed output")
@click.pass_context
def checklist_run(ctx, project_dir, dry_run, verbose):
    """Run checklist for project."""
    project_path = Path(project_dir).resolve()
    logger = StructuredLogger(project_path)

    engine = ChecklistEngine(project_path, logger)

    # Load manifest
    manifest_manager = ManifestManager(project_path)
    manifest = manifest_manager.load()

    # Load tasks
    engine.load_tasks_from_manifest(manifest)

    # Progress callback
    def on_progress(task: Task, step: Step):
        if verbose:
            click.echo(
                f"  [{int(task.progress):3d}%] {task.name}/{step.name}"
            )

    success = engine.run(
        dry_run=dry_run,
        on_progress=on_progress,
    )

    if success:
        click.echo("✓ Checklist completed")
    else:
        click.echo("✗ Checklist failed")
        click.exit(1)


