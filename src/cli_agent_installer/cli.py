@cli.command()
@click.argument("project_dir", type=click.Path())
@click.option("--checklist-id", help="Filter by checklist ID")
@click.option("--output", type=click.Path(), help="Export logs to file")
@click.option("--format", type=click.Choice(["json", "csv"]), default="json", help="Export format")
@click.pass_context
def logs(ctx, project_dir, checklist_id, output, format):
    """View or export structured logs."""
    project_path = Path(project_dir).resolve()
    logger = StructuredLogger(project_path)

    logs = logger.get_logs(checklist_id=checklist_id)

    if output:
        # Export to file
        if checklist_id:
            filepath = logger.export_audit_trail(checklist_id, output_format=format)
        else:
            # Export all logs
            filepath = Path(output)
            with open(filepath, "w") as f:
                json.dump([log.to_dict() for log in logs], f, indent=2)

        click.echo(f"✓ Logs exported to {filepath}")
    else:
        # Display to console
        if not logs:
            click.echo("No logs found")
            return

        click.echo(f"📜 Logs ({len(logs)} entries)")
        for log in logs:
            level_icon = {
                "DEBUG": "🔍",
                "INFO": "ℹ️",
                "WARNING": "⚠️",
                "ERROR": "❌",
                "CRITICAL": "🚨",
            }.get(log.level, "•")

            click.echo(
                f"  {log.timestamp} [{level_icon}] {log.type.value}: {log.message}"
            )


@cli.command()
@click.option("--host", default="127.0.0.1", help="Host to bind to")
@click.option("--port", default=8000, type=int, help="Port to bind to")
@click.option("--reload", is_flag=True, help="Enable auto-reload")
@click.pass_context
def serve(ctx, host, port, reload):
    """Start REST API server."""
    click.echo(f"🌐 Starting REST API server on {host}:{port}")

    try:
        import uvicorn
        from .api import app

        uvicorn.run(app, host=host, port=port, reload=reload)
    except ImportError:
        click.echo("❌ FastAPI and/or uvicorn not installed")
        click.echo("   Install with: pip install fastapi uvicorn")
        click.exit(1)


if __name__ == "__main__":
    cli()
